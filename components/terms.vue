            <p>ท่านมีสิทธิดำเนินการ ดังต่อไปนี้</p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 11.1
              สิทธิในการเพิกถอนความยินยอม (right to withdraw consent)
              ท่านมีสิทธิในการเพิกถอนความยินยอมในการประมวลผลข้อมูลส่วนบุคคลที่ท่านได้ให้ความยินยอมกับเราได้
              ตลอดระยะเวลาที่ข้อมูลส่วนบุคคลของท่านอยู่กับเรา
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 11.2&nbsp;
              สิทธิในการเข้าถึงข้อมูลส่วนบุคคล (right of access)
              ท่านมีสิทธิในการเข้าถึงข้อมูลส่วนบุคคลของท่านและขอให้เราทำสำเนาข้อมูลส่วนบุคคลดังกล่าวให้แก่ท่าน
              รวมถึงขอให้เราเปิดเผยการได้มาซึ่งข้อมูลส่วนบุคคลที่ท่านมิได้ให้ความยินยอมต่อเราได้
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 11.3&nbsp;
              สิทธิในการแก้ไขข้อมูลส่วนบุคคลให้ถูกต้อง (right to rectification):
              ท่านมีสิทธิในการขอให้เราแก้ไขข้อมูลที่ไม่ถูกต้อง
              หรือเพิ่มเติมข้อมูลที่ไม่สมบูรณ์
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 11.4&nbsp;
              สิทธิในการลบข้อมูลส่วนบุคคล (right to erasure)
              ท่านมีสิทธิในการขอให้เราทำการลบข้อมูลของท่านด้วยเหตุบางประการได้
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 11.5&nbsp;
              สิทธิในการระงับการใช้ข้อมูลส่วนบุคคล (right to restriction of processing)
              ท่านมีสิทธิในการระงับการใช้ข้อมูลส่วนบุคคลของท่านด้วยเหตุบางประการได้
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 11.6&nbsp;
              สิทธิในการโอนย้ายข้อมูลส่วนบุคคล (right to data portability)
              ท่านมีสิทธิในการโอนย้ายข้อมูลส่วนบุคคลของท่านที่ท่านได้ให้ไว้กับเราไปยังผู้ควบคุมข้อมูลรายอื่น
              หรือ ตัวท่านเองด้วยเหตุผลบางประการได้
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 11.7&nbsp;
              สิทธิในการคัดค้านการประมวลผลข้อมูลส่วนบุคคล (right to object)
              ท่านมีสิทธิในการคัดค้านประมวลผลข้อมูลส่วนบุคคลของท่านด้วยเหตุบางประการได้
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
              ท่านสามารถติดต่อมายังเจ้าหน้าที่ฝ่ายของเราได้
              เพื่อดำเนินการยื่นคำร้องขอดำเนินการตามสิทธิข้างต้นได้
              (รายละเอียดการติดต่อปรากฏในหัวข้อ &ldquo;ช่องทางการติดต่อ&rdquo;
              ด้านล่างนี้) หรือท่านสามารถศึกษารายละเอียดเงื่อนไข ข้อยกเว้นการใช้สิทธิต่าง
              ๆ ได้ที่ link รายละเอียดการใช้สิทธิ
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; ทั้งนี้&nbsp;
              ท่านไม่จำเป็นต้องเสียค่าใช้จ่ายใด ๆ ในการดำเนินการตามสิทธิข้างต้น
              โดยเราจะพิจารณาและแจ้งผลการพิจารณาตามคำร้องของท่านภายใน 30
              วันนับแต่วันที่เราได้รับคำร้องขอดังกล่าว
            </p>
            <ol start="12">
              <li>
                <strong><u>กิจกรรมทางการตลาดและการส่งเสริมการตลาด</u></strong>
              </li>
            </ol>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
              ในระหว่างการใช้บริการ&nbsp; เราจะส่งข้อมูลข่าวสารเกี่ยวกับกิจกรรมทางการตลาด
              และการส่งเสริมทางการตลาด&nbsp; ผลิตภัณฑ์&nbsp;
              การให้บริการของเราที่เราคิดว่าท่านอาจสนใจเพื่อประโยชน์ในการให้บริการอย่างเต็มประสิทธิภาพ&nbsp;
              หากท่านได้ตกลงที่จะรับข้อมูลข่าวสารดังกล่าวจากเราแล้ว&nbsp;
              ท่านมีสิทธิยกเลิกความยินยอมดังกล่าวได้ทุกเมื่อ&nbsp;
              โดยท่านสามารถดำเนินการยกเลิกความยินยอมในการรับแจ้งข้อมูลข่าวสารได้
            </p>
            <ol start="13">
              <li>
                <strong><u>การรักษาความมั่นคงปลอดภัยของข้อมูลส่วนบุคคล </u></strong>
              </li>
            </ol>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
              เรามีมาตรการรักษาความมั่นคงปลอดภัยของข้อมูลส่วนบุคคลของท่าน
              ตามมาตรฐานที่กฎหมายกำหนด
              เพื่อป้องกันมิให้ข้อมูลส่วนบุคคลที่ถูกเก็บรวบรวมสูญหายโดยอุบัติเหตุ
              หรือถูกเข้าถึง เปิดเผย หรือแก้ไขเปลี่ยนแปลงโดยปราศอำนาจหรือโดยมิชอบ
              อย่างไรก็ตาม
              เนื่องจากไม่มีวิธีการส่งข้อมูลทางอิเล็กทรอนิกส์หรือวิธีการจัดเก็บข้อมูลที่ปลอดภัยโดยสมบูรณ์
              เราจึงไม่สามารถรับประกันได้ว่าข้อมูลส่วนบุคคลของท่านจะปลอดภัยจากการรบกวนระหว่างการส่งหรือเก็บไว้ในระบบของเรา
              เพื่อช่วยปกป้องความเป็นส่วนตัวและการรักษาความลับของข้อมูลส่วนบุคคลของท่าน&nbsp;
              ความร่วมมือของท่านถือเป็นเรื่องสำคัญอย่างยิ่ง&nbsp;
              โปรดอย่าเปิดเผยรหัสผ่านของท่านแก่ผู้อื่นหรือใช้รหัสผ่านเดียวกันกับที่ท่านใช้สำหรับบริการ<br />
              อื่น ๆ
              และโปรดแจ้งให้เราทราบทันทีในกรณีที่ท่านสงสัยว่ามีการใช้บัญชีของท่านโดยไม่ได้รับอนุญาต
              หรือการละเมิดความปลอดภัยอื่น ๆ
              โดยท่านสามารถแจ้งกับเจ้าหน้าที่เพื่อติดต่อกับเรา&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            </p>
            <ol start="14">
              <li>
                <strong><u>การเปลี่ยนแปลงนโยบายคุ้มครองข้อมูลส่วนบุคคล</u></strong>
              </li>
            </ol>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
