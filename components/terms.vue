              เพื่อนำเสนอสินค้า บริการ และวิธีการใช้งานที่ดีและเหมาะสมแก่ท่าน
            </p>
            <ol start="8">
              <li>
                <strong><u>การประมวลผลข้อมูลส่วนบุคคล</u></strong>
              </li>
            </ol>
            <p>
              <strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong
              >เมื่อเราได้รับข้อมูลส่วนบุคคลจากแหล่งที่มาของข้อมูลส่วนบุคคลแล้ว
              เราจะดำเนินการดังนี้กับข้อมูลส่วนบุคคลของท่าน
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; -เก็บรวบรวมข้อมูล
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; -ใช้เพื่อสะสมแต้ม
              เพื่อแลกเครื่องดื่มฟรี หรือ รับส่วนลด หรือของพรีเมี่ยมจากทางร้านดาคาซี่
            </p>
            <ol start="9">
              <li>
                <strong><u>การแบ่งปันและการให้ข้อมูลส่วนบุคคล</u></strong>
              </li>
            </ol>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; โดยทั่วไปแล้ว
              เราจะไม่เปิดเผยหรือแชร์ข้อมูลของท่านให้แก่บุคคลภายนอกโดยไม่ได้รับความยินยอมจากท่าน
              หรือเป็นการปฏิบัติตามกฎหมายที่มีผลบังคับใช้ ยกเว้นการเปิดเผยข้อมูลของผู้ใช้
              ดังต่อไปนี้
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 9.1
              เมื่อผู้ใช้ได้ตกลงและยินยอมที่จะเปิดเผยหรือแบ่งปันข้อมูลของตนเอง
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 9.2
              เมื่อมีคำสั่งทางกฎหมาย
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 9.3 สิทธิของผู้ใช้
              ที่มอบอำนาจให้ผู้แทนทางกฎหมายดำเนินการแทนผู้ใช้
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 9.4
              ผู้ใช้บริการและผู้แทนทางกฎหมายของผู้ใช้บริการสามารถเรียกดูข้อมูลส่วนบุคคลเมื่อใดหรือแก้ไขข้อมูลส่วนบุคคลของผู้ใช้บริการและผู้แทนทางกฎหมายของผู้ใช้บริการที่ลงทะเบียนไว้
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 9.5
              ผู้ใช้บริการและผู้แทนทางกฎหมายของผู้ใช้บริการที่มีอายุน้อยกว่า 14 ปี
              อาจขอให้ลบบัญชีได้
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 9.6
              เมื่อผู้ใช้บริการขอแก้ไขเปลี่ยนแปลงข้อมูลส่วนบุคคลที่ไม่ถูกต้อง
              บริษัทจะไม่ใช้หรือให้ข้อมูลส่วนบุคคลแก่บุคคลที่สามจนกว่าจะได้รับการแก้ไข
              นอกจากนี้หากข้อมูลส่วนบุคคลที่ไม่ถูกต้องได้ถูกให้แก่บุคคลที่สามไปแล้ว
              บริษัทจะแจ้งให้บุคคลที่สามทราบถึงการแก้ไขทันที
              เพื่อที่จะทำการตรวจสอบให้แน่ใจว่าข้อมูลที่ได้รับนั้นถูกต้องหรือไม่
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 9.7
              การเปิดเผยข้อมูลส่วนบุคคลโดยได้รับการยกเว้นตามมาตรา 24 หรือ มาตรา 26
              แห่งพระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคล พ.ศ. 2562
            </p>
