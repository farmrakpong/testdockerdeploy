            <p>
              <strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong
              >ผู้ควบคุมทำการเก็บรักษาข้อมูลส่วนบุคคลของท่าน ดังนี้
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 10.1&nbsp;
              ลักษณะการเก็บ เราจะทำการเก็บข้อมูลเป็น Soft Copy
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 10.2&nbsp;
              สถานที่จัดเก็บ เราจะทำการเก็บข้อมูลไว้บน Cloud ที่ใช้บริการกับ Microsoft
              &nbsp;Window Cloud Software บริษัท Internet Thailand
            </p>
            <p>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 10.3&nbsp;
              ระยะเวลาการจัดเก็บ 10 ปี นับแต่วันที่เลิกสัญญา
            </p>
            <p>
              <strong><u>.</u></strong
              >&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; ตามกฎและนโยบายภายใน
              บริษัทจะช่วยลดการบันทึกของผู้ใช้ข้อมูลส่วนบุคคลที่ครั้งหนึ่งเคยเก็บรวบรวมและการใช้งานสำเร็จแล้ว
              อย่างไรก็ตามในกรณีที่บันทึกแล้วจะต้องมีการจัดเก็บไว้ตามนโยบายภายในของบริษัทหรือตามกฎหมายที่เกี่ยวข้อง
              บริษัทขอสงวนสิทธิ์ในการจัดเก็บข้อมูลส่วนบุคคลในช่วงเวลาหนึ่ง
            </p>
